    /**
     * 拖拽Video更换位置
     */
    private final View.OnTouchListener touchListener = new View.OnTouchListener() {
        int lastX, lastY, x, y;

        @Override
        public boolean onTouch(View v, MotionEvent e) {
            switch (MotionEvent.ACTION_MASK & e.getAction()) {
                case MotionEvent.ACTION_DOWN:
                    lastX = (int) e.getRawX();
                    lastY = (int) e.getRawY();
                    x = mParams.x;
                    y = mParams.y;
                    break;
                case MotionEvent.ACTION_MOVE:
                    int dx = (int) e.getRawX() - lastX;
                    int dy = (int) e.getRawY() - lastY;
                    mParams.x = x + dx;
                    mParams.y = y + dy;
                    mWindowManager.updateViewLayout(mFloatView, mParams);
                    break;
            }
            return true;
        }
    };

    private void addView() {
        if (mFloating) return;
        mParams.width = 400;//VideoView不能获取Video的宽高度
        mParams.height = 600;
        mWindowManager.addView(mFloatView, mParams);
        mFloating = true;

        startBackgroundThread();

        if (mTextureView.isAvailable()) {
            openCamera(mTextureView.getWidth(), mTextureView.getHeight());
        } else {
            mTextureView.setSurfaceTextureListener(mSurfaceTextureListener);
        }
    }

    private void removeView() {
        if (mFloating)
            mWindowManager.removeView(mFloatView);
        closeCamera();
        stopBackgroundThread();
        mFloating = false;
    }

